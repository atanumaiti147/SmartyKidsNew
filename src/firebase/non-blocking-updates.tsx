/**
 * Commits a WriteBatch operation.
 * Awaits the commit operation and handles permission errors.
 */
export async function commitBatch(batch: WriteBatch) {
    try {
        await batch.commit();
    } catch(error) {
         errorEmitter.emit(
            'permission-error',
            new FirestorePermissionError({
                path: 'batch operation',
                operation: 'write',
                requestResourceData: 'Multiple documents'
            })
        );
        // Re-throw the error so the calling function knows the batch failed
        throw error;
    }
}
